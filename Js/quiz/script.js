let index = 0;
const queBox = document.getElementById('display-question');
const optionLabels = [
    document.getElementById('option-one-label'),
    document.getElementById('option-two-label'),
    document.getElementById('option-three-label'),
    document.getElementById('option-four-label')
];

const options = document.querySelectorAll('.radio');
const nextQuestion = document.getElementById('nextQue');



function generateQuestion() {
    const data = questions[index];
    console.log(data);
    queBox.innerText = `${data.question}`;
    optionLabels[0].innerText = data.optionA
    optionLabels[1].innerText = data.optionB
    optionLabels[2].innerText = data.optionC
    optionLabels[3].innerText = data.optionD
}

generateQuestion();



const NextQuestion = nextQuestion.addEventListener('click', () => {
    getAns();
    if (index < questions.length - 1) {
        index++;
        generateQuestion();

        if (index === questions.length - 1) {
            nextQuestion.textContent = "Submit";
            nextQuestion.classList.add('submit');
            showScore();
        }


    }
});

function showScore() {
    const container = document.querySelector('.game-quiz-container');
    container.innerHTML = `<h1>Thank you for attempting the test</h1>
                           <h2>Your score: ${playerScore}</h2>
                           <p>Wrong Answers: ${wrongAnswers}</p>
                           <p>Right Answers: ${playerScore / 10}</p>
                           <p>Grade: ${(playerScore / (questions.length * 10)) * 100}%</p>`;
}



let playerScore = 0;
let wrongAnswers = 0;
function getAns() {
    const data = questions[index];
    let answered = false;
    options.forEach(option => {
        if (option.checked) {
            answered = true;
            if (option.value === data.correctOption) {
                playerScore += 10;
            } else {
                wrongAnswers++;
            }
        }
    });

    if (!answered) {
        alert('Please pick an option');
    }
}






